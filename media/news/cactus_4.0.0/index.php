<p>
We are pleased to announce release 4.0.0 of the Cactus computational toolkit.
This release includes new features like support for CUDA compilers, support for
new architectures and comes bundled with supporting tools like GetComponents
and Simfactory. Also, CactusExternal is now deprecated in favor of the better
infrastructure in ExternalLibraries. In addition, bug fixes accumulated since
the previous release in February 2009 have been included.</p>

<p>Read more about the new release in the longer <a href="http://cactuscode.org/download/releasenotes/Cactus_4.0.0">release notes</a>.</p>


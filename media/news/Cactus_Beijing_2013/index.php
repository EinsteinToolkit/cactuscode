<p>
 Brandt and Löffler presented Cactus and the
 <a href="http://einsteintoolkit.org/">Einstein Toolkit</a> to faculty of the
 <a href="http://www.tsinghua.edu.cn/publish/csen/">Department of Computer
 Science and Technology</a> at Tsinghua University, at the <a
 href="http://lsec.cc.ac.cn/">State Key Laboratory of Scientific and
 Engineering Computing (LSEC)</a>, at the <a
 href="http://www.iapcm.ac.cn/">Institute of Applied Physics and Computational
 Mathematics</a>, and to a high-performance-computing class at Tsinghua
 University.

<p>
The Cactus Computational Toolkit has been showcased again by the <a href="http://www.cct.lsu.edu">Center for Computation & Technology</a>(CCT) researchers at the annual <a href="http://sc07.supercomputing.org/">Supercomputing Conference 2007</a> (SC07) from Reno, Nevada. SC07 is an international conference on high performance computing, networking, storage and analysis.
</p>
<p>
The conference is an exciting opportunity for scientists, researchers, software developers, network engineers, policy makers, corporate managers, CIOs and IT administrators from universities, industry and government from all over the world to come together and present their results and technologies they develop.
</p>
<p>
Erik Schnetter showed the first large scale runs on <a href="http://www.hpc.lsu.edu/systems/system.php?system=QueenBee">QueenBee</a> in a presentation entitled <a href="http://www.cct.lsu.edu/~eschnett/doc/Reno-2007-Supercomputing-CactusPetascale.pdf">Cactus Tools for Petascale Copmuting</a>. Scaling results of the single-node and of all the 668 nodes of the Einstein Equations can be found in the presentation. At the end, Dr. Schnetter showed an interactive demo on Queen Bee.
</p>
<p>
Chris White showed a <a href="http://www.ucoms.org/">UCoMs</a> demo black oil and Mayank Tyagi talked about the <i>Computational Fluid Dynamics Toolkit in Cactus</i>.
</p>

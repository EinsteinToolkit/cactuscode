<p>
<strong>March 2007: </strong>
Scientists from Lawrence Berkeley National Laboratory and Princeton Plasma Physics Laboratory have chosen Cactus to be one of the six scientific applications used in the evaluation of the performance of six of the world's fastest supercomputers.
</p>

<p>
Cactus has successfully demonstrated impressive scalability up to 32K processors on the BGW system and showed extremely promising results, suggesting that the application will scale to larger petascale systems.
</p>

<p>
More about the experimental results in <a href="http://crd.lbl.gov/~oliker/papers/ipdps07.pdf"><i> "Scientific Application Performance on Candidate PetaScale Platforms" </i></a>, winner of the Best Paper Award, International Parallel & Distributed Processing Symposium (IPDPS), March 24-30, 2007, Long Beach, CA.
</p>


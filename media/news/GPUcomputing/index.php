<p>
Using the Cactus Computational Toolkit, CCT researcher, Dr. Burkhard Zink, solved Albert Einstein's field equations for general relativistic gravitation on a graphical processing unit (GPU). The benchmark results of his work show that the GPU can improve the computation of finite difference codes solving these equations. 
</p>
<p>
Dr. Zink's work is part of a larger project in which researchers and NVIDIA collaborate on <a href="http://www.hpcwire.com/hpc/2092380.html">GPU Petascale Computing</a>.
</p>
<p>
More information about his results can be found in the CCT Technical Report Series, 2008, <a href="http://www.cct.lsu.edu/CCT-TR/CCT-TR-2008-1">"A General Relativistic Evolution Code on CUDA Architectures"</a>
</p>


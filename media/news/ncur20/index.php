<p>
CCT undergraduate students had the opportunity to present results of their research at NCUR-20.
</p>

<p><b>April 2006:</b><br />
In April 2006, seven undergraduate students who worked for the CCT were invited to present their research projects at NCUR, the National Conference for Undergraduate Research, hosted this year at the University of North Carolina at Asheville. The conference, which attracts yearly students from over 300 colleges and universities across the United States, promotes undergraduate research scholarship and creative activity done in partnership with faculty or other mentors.
</p>

<p>
Four papers discussing the results of different implementations in Cactus had been written by our students for NCUR.
</p>

<p>
Josh Abadie had implemented a thorn providing one of the Sandia National Laboratories' self-contained interoperable packages: Trilinos, whose primary purpose is to ease integration of different mathematical software libraries. With this project several packages have been made accessible to the Cactus user community to provide a broad range of solvers. Results of this work are discussed in <a href ="Cactus_Abadie06.pdf">“Integration of Trilinos Into The Cactus Code Framework”</a>.
</p>

<p>
Jeff DeReus’ implementation of binary trees in Cactus provides capabilities for new application domains. The core module, discussed in his paper <a href ="Cactus_DeReus06.pdf">“Implementation of a Binary Tree Driver (OAKc) in Cactus”</a>, is special in that it can allocate binary tree nodes for other thorns to use, in effect realizing the role of a Cactus driver thorn.
</p>

<p>
Razvan Carbunescu wrote about <a href= "Cactus_Carbunescu06.pdf">“Implementation of Level Set Methods in Cactus Framework”</a>.  Level set methods represent a good way of representing the movement of fluids or objects in evolving velocity fields. “I chose to implement this in the Cactus Framework because of the great flexibility, portability and scalability of Cactus with comparison with other frameworks. My paper presented the results of applying the implemented level set methods on a rotating disc (Zalesak's problem), which is a usual problem that tests Level Set Methods”, says Razvan.
</p>

<p>
Ian Wesley-Smith's paper presents results of <a href="Cactus_WesleySmith06.pdf">“A Parallel Artificial Neural Network Implementation”</a> in Cactus, as well as the network's relative strengths and weaknesses.
Artificial neural networks are massively parallel systems capable of learning and making generalizations. The inherent parallelism in the network allows for a distributed software implementation of the artificial neural network, causing the network to learn and operate in parallel, theoretically resulting in a performance improvement.
</p>

<p>
Each of these students has his paper published in the Proceedings of the National Council for Undergraduate Research.  
</p>


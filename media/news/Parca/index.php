<p><b>April 2007:</b></p>

<p>
NASA awarded another <a href="http://www.sbir.nasa.gov/SBIR/abstracts/05/sttr/phase2/STTR-05-2-T4.02-9864.html">STTR grant </a> for the "Interfacing the Paramesh Computational Libraries to the Cactus Computational Framework" proposal. 
<p>During the <a href="/media/news/Paramesh">first phase</a> of the project, researchers at CCT - where the PI is Dr. Manuel Tiglio - and <a href="http://www.dac.us/"> Decisive Analytics Corporation</a>  developed an interface between the Paramesh computational libraries and the Cactus computational toolkit. 
</p>

<p>
The project enhances and broadens the collaboration between the computational fluid dynamics and numerical general relativity communities, having a significant importance for NASA.
